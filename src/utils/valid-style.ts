export const validStyle = {
  view,
  text,
};

export function validateStyleProps<T extends Record<string, any>>(props: T, style: string[] = validStyle.view) {
  return Object.keys(props)
    .filter(item => style.includes(item))
    .reduce((acc, item) => {
      const prop = props[item];
      if (typeof props[item] === 'function') return acc;
      if (typeof props[item] === 'undefined') return acc;
      acc[item] = prop;
      return acc;
    }, {} as any) as T;
}
